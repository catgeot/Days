/**
 * place_alias 시드 SQL 생성
 *
 *   node scripts/generate-place-alias-seed.mjs
 *   node scripts/generate-place-alias-seed.mjs --write
 */
import { mkdirSync, writeFileSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';
import { TRAVEL_SPOTS } from '../src/pages/Home/data/travelSpots.js';
import { citiesData } from '../src/pages/Home/data/citiesData.js';
import { KEYWORD_DB } from '../src/pages/Home/data/keywordData.js';
import {
  TRAVEL_SPOT_PLACE_ID_ALIASES,
  TRAVEL_SPOT_TOOLKIT_SYNONYMS,
} from './data/travel-spot-place-id-aliases.mjs';
import { buildSpotLookup, normalizePlaceKey } from './lib/travel-spot-place-resolve.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT_DIR = join(__dirname, '../supabase/migrations');
const OUT_PATH = join(OUT_DIR, '20260521120100_seed_place_alias.sql');

function escapeSql(s) {
  return String(s).replace(/'/g, "''");
}

function addAlias(map, alias, slug, source) {
  const a = String(alias ?? '').trim();
  const s = String(slug ?? '').trim().toLowerCase();
  if (!a || !s || a.length > 200) return;
  const key = normalizePlaceKey(a);
  if (!key) return;
  if (!map.has(key)) map.set(key, { alias: a, canonical_slug: s, source });
}

function main() {
  const map = new Map();
  const lookup = buildSpotLookup(TRAVEL_SPOTS);
  const slugs = new Set(TRAVEL_SPOTS.map((s) => s.slug));

  for (const [alias, slug] of Object.entries(TRAVEL_SPOT_PLACE_ID_ALIASES)) {
    if (slugs.has(slug)) addAlias(map, alias, slug, 'travel_spot_alias');
  }

  for (const [slug, synonyms] of Object.entries(TRAVEL_SPOT_TOOLKIT_SYNONYMS)) {
    if (!slugs.has(slug)) continue;
    addAlias(map, slug, slug, 'toolkit_synonym');
    for (const syn of synonyms || []) addAlias(map, syn, slug, 'toolkit_synonym');
  }

  for (const spot of TRAVEL_SPOTS) {
    addAlias(map, spot.name, spot.slug, 'travel_spots');
    addAlias(map, spot.name_en, spot.slug, 'travel_spots');
    addAlias(map, spot.slug, spot.slug, 'travel_spots');
    for (const kw of spot.keywords || []) addAlias(map, kw, spot.slug, 'search_keys');
  }

  for (const city of citiesData) {
    if (!city.slug) continue;
    addAlias(map, city.name, city.slug, 'cities_data');
    addAlias(map, city.name_en, city.slug, 'cities_data');
    addAlias(map, city.slug, city.slug, 'cities_data');
  }

  for (const values of Object.values(KEYWORD_DB)) {
    for (const name of values) {
      const resolved = lookup.get(normalizePlaceKey(name));
      if (resolved?.slug) addAlias(map, name, resolved.slug, 'keyword_data');
    }
  }

  const rows = [...map.values()].sort((a, b) =>
    a.canonical_slug.localeCompare(b.canonical_slug) || a.alias.localeCompare(b.alias)
  );

  const lines = [
    '-- Generated by scripts/generate-place-alias-seed.mjs — do not hand-edit',
    `-- ${rows.length} aliases`,
    '',
    'INSERT INTO public.place_alias (alias, canonical_slug, source)',
    'VALUES',
  ];

  const valueLines = rows.map(
    (r) => `  ('${escapeSql(r.alias)}', '${escapeSql(r.canonical_slug)}', '${escapeSql(r.source)}')`
  );
  lines.push(valueLines.join(',\n'));
  lines.push('ON CONFLICT (alias) DO UPDATE SET');
  lines.push('  canonical_slug = EXCLUDED.canonical_slug,');
  lines.push('  source = EXCLUDED.source;');
  lines.push('');

  const sql = lines.join('\n');
  const write = process.argv.includes('--write');

  if (write) {
    mkdirSync(OUT_DIR, { recursive: true });
    writeFileSync(OUT_PATH, sql, 'utf8');
    console.log(`Wrote ${rows.length} aliases → ${OUT_PATH}`);
  } else {
    console.log(`[dry] Would write ${rows.length} aliases → ${OUT_PATH}`);
    console.log('Run with --write to create migration file.');
  }
}

main();
