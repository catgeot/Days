export function formatReleaseDate(release) {
  const raw = release?.date ?? release?.id ?? '';
  const match = String(raw).match(/^(\d{4})-(\d{2})-(\d{2})/);
  if (!match) return raw;
  const [, year, month, day] = match;
  return `${year}. ${Number(month)}. ${Number(day)}.`;
}
